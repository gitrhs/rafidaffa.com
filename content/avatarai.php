        <h3>Project Overview</h3>
        <div class="blog-content-body">
            A system for real-time video conversations in
            Bahasa Indonesia. It combines natural language
            processing with a deepfake avatar to simulate
            face-to-face interaction. Users ask questions
            verbally, the system processes and retrieves the
            most relevant answers, then responds through a
            humanlike avatar with synchronized speech,
            mimicking the experience of talking with a real
            person.
        </div>
        <br />
        <h3>How it works? (the pipeline)</h3>
        <img src="image/avatarAI/avatarAI-flow-Fixed.png" class="blog-img" />
        <div class="blog-content-body">
            The process starts when a user asks a question
            verbally. The audio is transcribed into text,
            which is then converted into vector embeddings
            to capture its meaning. These embeddings are
            searched against a vector database, and the top
            three most relevant answers are returned.
        </div>
        <div class="blog-content-body">
            A large language model refines these answers
            into a coherent script. The script is converted
            into audio, which is then paired with a deepfake
            avatar to produce synchronized video
            outputâ€”delivering the response as if spoken by a
            real person in a live conversation.
        </div>
