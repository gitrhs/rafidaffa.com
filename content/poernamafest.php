        <h3>Project Overview</h3>
        <div class="blog-content-body">
            PoernamaFest is an annual cultural festival that
            celebrates the beauty and diversity of cultures
            from around the world, with a special focus on
            Indonesian music and heritage. It brings people
            together through music, art, food, and cultural
            activities such as workshops and performances,
            creating a space for everyone to learn, share,
            and enjoy. Performers from different countries,
            including Malaysia, honor Indonesian culture by
            singing Indonesian songs, making the festival a
            unique and memorable experience for all.
        </div>
        <div class="blog-content-body">
            As the full-stack developer and UI/UX designer,
            I worked on building the PoernamaFest website as
            the central hub for event information. The goal
            was to make it easier for people to learn about
            and participate in the festival, while also
            strengthening brand identity and boosting ticket
            sales.
        </div>
        <br />
        <h3>Website Guidelines</h3>
        <img src="image/poernamafest/1.jpg" class="blog-img" />
        <img src="image/poernamafest/2.jpg" class="blog-img" />
        <div class="blog-content-body">
            Website Guidelines of PoernamaFest, published as
            a reference for building and maintaining the
            festival website. The guideline covers six key
            areas: Principles, Logo, Color Palette,
            Typography, Icon, and Framework.
        </div>
        <br />
        <h3>Principles</h3>
        <img src="image/poernamafest/3.jpg" class="blog-img" />
        <div class="blog-content-body">
            The website serves as the central platform for
            festival-related information, providing
            ticketing and registration, performer profiles,
            news, and sponsorship opportunities. Consistency
            is maintained through a recurring moon motif and
            a color scheme of blue and white. Responsiveness
            ensures seamless access across devices, with
            layouts and elements dynamically adjusting to
            different screen sizes.
        </div>
        <br />
        <h3>Logo</h3>
        <img src="image/poernamafest/4-2.jpg" class="blog-img" />
        <div class="blog-content-body">
            The Poernama Bloom logo is based on the full
            moon, symbolizing unity, abundance, and
            celebration. The circular form and hand-drawn
            typography reflect the festival’s vibrant,
            inclusive spirit.
        </div>
        <br />
        <h3>Color Palette</h3>
        <img src="image/poernamafest/5.jpg" class="blog-img" />
        <div class="blog-content-body">
            The main palette uses gradients of moderate blue
            and strong blue for backgrounds, combined with
            light grayish orange for text. This creates
            contrast and harmony while staying aligned with
            the night-sky theme.
        </div>
        <br />
        <h3>Typography</h3>
        <img src="image/poernamafest/6.jpg" class="blog-img" />
        <img src="image/poernamafest/7.jpg" class="blog-img" />
        <div class="blog-content-body">
            Magic Romance is used for titles, giving a
            whimsical and artistic feel that resonates with
            the festival’s identity. Roboto is used for body
            text, ensuring clarity, readability, and balance
            across the site.
        </div>
        <br />
        <h3>Iconography</h3>
        <img src="image/poernamafest/8.jpg" class="blog-img" />
        <div class="blog-content-body">
            Font Awesome provides scalable vector icons for
            navigation and actions. The icons maintain a
            consistent look, improve usability, and keep the
            site lightweight.
        </div>
        <br />
        <h3>Framework & Stack</h3>
        <img src="image/poernamafest/9.jpg" class="blog-img" />
        <div class="blog-content-body">
            The website is built with HTML, CSS, JavaScript,
            PHP, and MySQL, supported by Bootstrap as the
            main framework and jQuery as the library. This
            ensures scalability, responsiveness, and
            efficient performance.
        </div>
