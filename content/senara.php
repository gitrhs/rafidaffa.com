        <h3>Project Overview</h3>
        <div class="blog-content-body">
            SenaraFest, or SENARA Creative Artfest, is a
            major cultural event organized by the Indonesian
            student community in Malaysia. It is designed as
            a platform for creative expression, open not
            only to students but also to communities and the
            general public. With an estimated 1,000+
            visitors over two days, 500+ participants, and a
            digital reach exceeding 10,000 accounts, the
            festival offers partners strong opportunities to
            expand their brand image and audience reach.
        </div>
        <div class="blog-content-body">
            The event brings together diverse creative
            platforms including art competitions, handicraft
            exhibitions, band performances, and hands-on
            workshops. These activities create meaningful
            cross-cultural collaboration, connecting
            artists, creative industry players, students,
            and communities from both local and regional
            networks.
        </div>
        <div class="blog-content-body">
            As full-stack developer and UI/UX designer, I
            built the SenaraFest website as the main hub for
            the event. The platform allows artists to submit
            their works, visitors to purchase tickets, and
            sponsors to engage directly with the festival.
            The design reflects the creative and inclusive
            spirit of the festival, while making
            participation and collaboration accessible to
            all.
        </div>
