        <h3>Project Overview</h3>
        <div class="blog-content-body">
            UM Notes is a digital platform built exclusively
            for students of the University of Malaya (UM).
            It serves as a resource hub where students can
            share lecture notes, past papers, study guides,
            and even general files that may be useful to the
            academic community. Access is restricted to UM
            students, who log in using their university
            email accounts. The project was developed as
            part of the PPI-UM work program and the
            <i>System Analysis and Design</i> course, where
            I worked as both full-stack developer and UI/UX
            designer.
        </div>
        <br />
        <h3>How it works?</h3>
        <div class="blog-content-body">
            The platform allows students to upload files
            through a simple process. A file is first hosted
            on a cloud storage service such as Google Drive,
            OneDrive, or Dropbox, and then linked through
            the UM Notes dashboard. Once a student submits
            the file, it is reviewed by a contributor or
            admin before being published to ensure quality
            and relevance. This verification step keeps the
            platform organized and reliable for all users.
        </div>
        <br />
        <h3>Community Features</h3>
        <div class="blog-content-body">
            UM Notes emphasizes collaboration and
            contribution. Students who upload files earn
            reward points, which can be collected and
            exchanged for items through the rewards page.
            Contributors play a special role in the
            ecosystem by verifying pending files, creating
            new folders, and ensuring the platform remains
            structured and easy to navigate. Their names are
            displayed on the resources they approve, giving
            them visibility and recognition within the UM
            community. A leaderboard highlights the top
            contributors, encouraging active participation
            and fostering a sense of ownership.
        </div>
        <br />
        <h3>Impact</h3>
        <div class="blog-content-body">
            Since its launch, UM Notes has become one of the
            reliable platforms for UM students to access
            course references. It centralizes resources that
            would otherwise be scattered across private
            groups and personal drives, while also
            motivating students to give back to the
            community. By combining accessibility,
            collaboration, and gamified rewards, the
            platform creates a sustainable model for
            knowledge-sharing within the university.
        </div>
