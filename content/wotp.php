        <h3>Project Overview</h3>
        <video src="https://docs.rafidaffa.com/blog/video/whisper-of-the-path/presentation-1.mp4" type="video/mp4"
            controls playsinline class="blog-img"></video>
        <div class="blog-content-body">
            Whispers of the Path is a virtual reality game
            developed as a project for WIG3004 - Virtual
            Reality at University Malaya. Working within a
            one-week timeframe, we created a complete VR
            adventure experience from concept to playable
            demo.
        </div>
        <div class="blog-content-body">
            A lost soul wakes up in the Realm of Echoes, a
            world between sleep and consciousness. A
            lifeless tree and a wise peasant guide the
            player to find an ancient portal that leads to
            the Singing Stone. The Singing Stone is the way
            for players to return home.
        </div>
        <div class="blog-content-body">
            The primary objective of this game is to explore
            the open world of the Realm of Echoes, interact
            with mystical elements, and find the ancient
            portal leading to the Singing Stone to escape
            this dreamlike dimension. Players move through a
            VR environment where they discover the story
            through exploration. The game focuses on
            environmental storytelling where players learn
            about the world by walking around and finding
            clues.
        </div>
        <br />
        <h3>Characters & Avatars</h3>
        <video src="https://docs.rafidaffa.com/blog/video/whisper-of-the-path/presentation-2.mp4" type="video/mp4"
            controls playsinline class="blog-img"></video>
        <div class="blog-content-body">
            We created virtual versions of ourselves as the
            main characters. Alyssa's avatar wears
            traditional batik clothing, and Rafi's avatar
            wears Indonesian cultural clothing. We also made
            The Peasant, an NPC character who guides players
            and explains the secrets of the Realm of Echoes.
        </div>
        <div class="blog-content-body">
            We built the characters by first creating an
            armature with bones for movement. We added bones
            for the head, torso, arms, and legs so the
            characters can move naturally. We used weight
            painting to control which parts of the character
            move with each bone. We tested the character
            movement in pose mode to make sure everything
            worked correctly before putting them in the
            game.
        </div>
        <br />
        <h3>Game World & Assets</h3>
        <video src="https://docs.rafidaffa.com/blog/video/whisper-of-the-path/presentation-3.mp4" type="video/mp4"
            controls playsinline class="blog-img"></video>
        <div class="blog-content-body">
            The VR world looks like real environments but
            with some fantasy elements. We added grass and
            trees that move in the wind to make the world
            feel more alive. The environment mixes normal
            nature with fantasy elements from the Realm of
            Echoes.
        </div>
        <div class="blog-content-body">
            The game world includes grass and evergreen
            trees that move with wind animation. We added
            fantasy elements like the Rock Portal and
            Singing Stones that are important for gameplay.
            We also built rock formations with lighting and
            shadows, plus different textures to make the
            environment look varied.
        </div>
        <div class="blog-content-body">
            Players can walk around the entire game world
            freely while following the main story to reach
            the Singing Stone. This open-world setup lets
            players explore at their own speed without being
            forced to follow a specific path.
        </div>
        <br />
        <h3>Immersive Experience</h3>
        <video src="https://docs.rafidaffa.com/blog/video/whisper-of-the-path/presentation-4.mp4" type="video/mp4"
            controls playsinline class="blog-img"></video>
        <div class="blog-content-body">
            The game uses dynamic lighting and shadows to
            create depth. The graphics run at up to 120fps
            so players don't get motion sickness. We added
            environmental effects and atmospheric rendering
            to make the world look more realistic.
        </div>
        <div class="blog-content-body">
            We added different footstep sounds that change
            depending on what surface players walk on -
            grass, water, or other materials. Background
            music plays throughout the game, and we included
            character dialogue sounds and special audio
            effects for the portals. These sounds help make
            the world feel more real.
        </div>
        <div class="blog-content-body">
            The VR controllers vibrate when players enter
            the portal, giving physical feedback. For
            character conversations, we used Animal
            Crossing-style sound effects instead of full
            voice acting. Players move around using
            teleportation, which prevents motion sickness.
            The story is told through the environment -
            players learn by exploring and finding things in
            the world.
        </div>
        <div class="blog-content-body">
            This VR game runs on VR headsets with
            performance up to 120fps for comfortable
            gameplay. We used teleportation for player
            movement to avoid motion sickness. The audio
            system uses 3D spatial sound so players can hear
            where sounds come from in the virtual space. The
            graphics use dynamic lighting that changes based
            on the game environment.
        </div>
        <br />
        <h3>Game Ending</h3>
        <video src="https://docs.rafidaffa.com/blog/video/whisper-of-the-path/presentation-5.mp4" type="video/mp4"
            controls playsinline class="blog-img"></video>
        <div class="blog-content-body">
            The game ends when players enter the final
            portal. This triggers an ending video that shows
            the player waking up from a dream. The twist
            reveals that the entire game was actually a
            dream. This ending explains why the Realm of
            Echoes felt strange and connects the dream-like
            game world back to reality.
        </div>
